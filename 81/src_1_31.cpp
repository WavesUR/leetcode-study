class Solution {
public:
    int search(vector<int>& nums, int target) {
        if(nums.empty()){
          return false;
        }
        int left = 0, right = nums.size() - 1, mid;
        while(left <= right){
          mid = left + (right - left) /2;
          if(nums[mid] == target){
            return true;
          }else if(nums[mid] > nums[right]){
            if(target >= nums[left] && target < nums[mid]){
              right = mid - 1;
            }else{
              left = mid + 1;
            }
          }else if(nums[mid] < nums[right]){
            if(target <= nums[right] && target > nums[mid]){
              left = mid + 1;
            }else{
              right = mid - 1;
            }
          }else{
            right--;
          }
        }
        return false;
    }
};

